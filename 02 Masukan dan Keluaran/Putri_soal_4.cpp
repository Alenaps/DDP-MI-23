#include <iostream>
using namespace std;

int main () {
    float  c,  d;
    cin >> c >> d;

    float penjumlahan = c + d;
    float pengurangan = c - d;
    float perkalian = c * d;
    float pembagian = c / d;

    cout << "Hasil dari Penjumlahan " << c << " dengan " << d << " adalah " << penjumlahan << endl;
    cout << "Hasil dari Pengurangan " << c << " dengan " << d << " adalah " << pengurangan << endl;
    cout << "Hasil dari Perkalian " << c << " dengan " << d << " adalah " << perkalian << endl;
    cout << "Hasil dari Pembagian " << c << " dengan " << d << " adalah " << pembagian << endl;

return 0;

}
